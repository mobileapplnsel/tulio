            Closing your web browser erases the session cookies. This type of cookie helps the site to
            function correctly, for example, to expand and collapse menus, make the Tulio font appear
            properly on your screen, or save your work as you progress through a form. Persistent
            cookies are also stored in your computer, and they will persist even after you close your
            browser. How long they persist depends on how they were first set up; the period can vary
            from minutes to years. Persistent cookies are used to store the preferences of your current
            visit, to be used for future visits (such as remembering your location or language
            preferences, areas of interest, or other settings to customize your experience). They also help
